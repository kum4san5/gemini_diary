/**
 * Geminiに送るための添削プロンプトを生成する関数
 * @param {string} text - お友達から届いた英文
 * @return {string} 生成されたプロンプト
 */
function getFixEnglishPrompt(text) {
  
  // 💡 ユーザーが作ってくれた最高のプロンプトをそのままGAS用にセット！
  const prompt = `【厳格なルール：必ず守ること】
1. あなたは優しい英語の先生です。以下の【日記本文】だけを読んで、不自然な場所の修正案と、日本語での解説・応援コメントを親切に作成してください。
2. 挨拶や導入は最小限にし、ダラダラと長い解説は避けてください。
3. 他の架空の例文や、長文の添削サンプルなどは絶対に一切出力しないでください。
4. 返答の全体文字数は、英語と日本語を合わせて必ず「1000文字程度」で綺麗に完結させてください。2000文字を超えてはいけません。

出力は必ず以下のJSON形式のみで行ってください。 
{ "answer":"回答","corrected_text": "修正後の英文のみ", "feedback": "修正のポイントやアドバイスのみ" }


【日記本文】
${text}`;

  return prompt;
}